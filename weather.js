async function getWeatherForecast(regionName = "서울") {
  const regId = REGION_MAP[regionName] || REGION_MAP["서울"];

  if (!KMA_API_KEY) {
    return {
      ok: false,
      error: "KMA_API_KEY가 설정되어 있지 않습니다. .env를 확인하세요.",
    };
  }

  try {
    const url = `${KMA_API_URL}?tmfc=0&authKey=${KMA_API_KEY}`;
    const response = await fetch(url);
    
    if (!response.ok) {
      throw new Error(`API 요청 실패: ${response.status}`);
    }
    
    const text = await response.text();
    
    // 텍스트 데이터 파싱
    const lines = text.split("\n");
    let startIdx = -1;
    
    // 헤더 찾기
    for (let i = 0; i < lines.length; i++) {
      if (lines[i].includes("REG_ID") && lines[i].includes("REG_NAME")) {
        startIdx = i + 1;
        break;
      }
    }
    
    if (startIdx === -1) {
      throw new Error("API 응답 형식이 올바르지 않습니다.");
    }
    
    // 해당 지역 찾기
    let regionData = null;
    for (let i = startIdx; i < lines.length; i++) {
      const line = lines[i].trim();
      if (!line || line.startsWith("#")) continue;
      
      const parts = line.split(/\s+/);
      if (parts.length >= 5 && parts[0] === regId) {
        regionData = {
          regId: parts[0],
          regionName: parts.slice(4).join(" "),
        };
        break;
      }
    }
    
    if (!regionData) {
      throw new Error(`${regionName} 지역을 찾을 수 없습니다.`);
    }
    
    return {
      ok: true,
      region: regionName,
      regionData,
    };
  } catch (err) {
    return {
      ok: false,
      error: String(err.message || err),
    };
  }
}

async function formatWeatherMessage(regionName = "서울") {
  const result = await getWeatherForecast(regionName);
  
  if (!result.ok) {
    return `오류: ${result.error}`;
  }
  
  // 지역명 정규화
  const displayRegion = regionName || "서울";
  const regionFullName = result.regionData?.regionName || displayRegion;
  const nowKST = new Date().toLocaleString("ko-KR", { timeZone: "Asia/Seoul" });
  
  // 날씨 정보 포맷팅
  const message = `
🌤️ **${displayRegion} 날씨 정보**

**지역명**: ${regionFullName}
**조회 시간**: ${nowKST}
**데이터 출처**: 한국 기상청

기상청 날씨 정보를 조회했습니다. 
더 상세한 예보는 [기상청 홈페이지](https://www.kma.go.kr)를 참고해주세요.
`;
  
  return message;
}

